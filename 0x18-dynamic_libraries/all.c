#include <stdio.h>
/**
 * div - divides int
 * @: 1st int
 * @b 2nd int
 * Return: output
 */
int div(int a, int b)
{
	return (a / b);
}
/**
 * mod - mod int
 * @: 1st int
 * @b 2nd int
 * Return: output
 */
int mod(int a, int b)
{
	return (a % b);
}
/**
 * sub - sub int
 * @: 1st int
 * @b 2nd int
 * Return: output
 */
int sub(int a, int b)
{
	return (a - b);
}
/**
 * mul - multiply int
 * @: 1st int
 * @b 2nd int
 * Return: output
 */
int mul(int a, int b)
{
	return (a * b);
}
/**
 * add - add int
 * @: 1st int
 * @b 2nd int
 * Return: output
 */
int add(int a, int b)
{
	return (a + b);
}
